function liveRelativeTime(iso) {
  if (!iso) return "";
  const diff = Date.now() - new Date(iso).getTime();
  if (isNaN(diff) || diff < 0) return "just now";
  const s = Math.floor(diff / 1000);
  const m = Math.floor(diff / 60000);
  const h = Math.floor(diff / 3600000);
  const d = Math.floor(diff / 86400000);
  if (s < 60)  return "just now";
  if (m < 60)  return `${m}m ago`;
  if (h < 24)  return `${h}h ago`;
  if (d < 30)  return `${d}d ago`;
  return new Date(iso).toLocaleDateString("en-IN", { day:"numeric", month:"short", year:"numeric" });
}

function getPreview(fullText, summary, headline) {
  const src = (fullText || summary || "").trim();
  if (!src || src.length < 30) return "";
  const flat = src.replace(/\n+/g, " ").replace(/\s+/g, " ").trim();
  const sentences = flat
    .split(/(?<=[.!?])\s+(?=[A-Z"'(])/)
    .map(s => s.trim())
    .filter(s => s.length > 20);
  if (!sentences.length) return "";
  const hlKey = (headline || "").toLowerCase().replace(/[^a-z0-9]/g, "").slice(0, 50);
  const s0Key = sentences[0].toLowerCase().replace(/[^a-z0-9]/g, "").slice(0, 50);
  const skipFirst = hlKey.length > 10 && (s0Key.startsWith(hlKey.slice(0,30)) || hlKey.startsWith(s0Key.slice(0,30)));
  const preview = sentences.slice(skipFirst ? 1 : 0, (skipFirst ? 1 : 0) + 2).join(" ").trim();
  if (!preview || preview.length < 20) return "";
  return preview.length > 200 ? preview.slice(0, 197) + "…" : preview;
}

function cleanHeadline(h) {
  return (h || "").replace(/\s[-–|]\s*[A-Z][a-zA-Z\s.&]{2,}$/, "").trim() || h;
}

